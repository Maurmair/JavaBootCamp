package Chapter5;

public class Exc6 {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;

        System.out.println("Number 1: " + a);
        System.out.println("Number 2: " + b);

        System.out.println("1 > 2:\t" + (a > b));
        System.out.println("1 >= 2:\t" + (a >= b));
        System.out.println("1 < 2:\t" + (a < b));
        System.out.println("1 <= 2:\t" + (a <= b));
        System.out.println("1 == 2:\t" + (a == b));
        System.out.println("1 != 2:\t" + (a != b));
    }
}
