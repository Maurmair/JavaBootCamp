package chapter5;

public class Exc7 {
    public static void main(String[] args) {
        boolean a = false;
        boolean b = true;

        System.out.println("a:\t" + a);
        System.out.println("b:\t" + b);
        System.out.println("a && b:\t"+(a&&b));
        System.out.println("a || b:\t"+(a||b));
        System.out.println("!a:\t"+(!a));
        System.out.println("!b:\t"+(!b));
    }
}
